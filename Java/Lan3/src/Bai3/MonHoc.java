package Bai3;

public class MonHoc {
    private String ma, ten;
    private int soTC;

    public MonHoc(String ma, String ten, int soTC) {
        this.ma = ma;
        this.ten = ten;
        this.soTC = soTC;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }
}
